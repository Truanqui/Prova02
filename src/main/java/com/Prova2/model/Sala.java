package com.Prova2.model;

public class Sala {
    private int idSala;
    private int Sala;
    private int Assentos;

    public int getIdSala() {
        return idSala;
    }

    public void setIdSala(int idSala) {
        this.idSala = idSala;
    }

    public int getSala() {
        return Sala;
    }

    public void setSala(int sala) {
        Sala = sala;
    }

    public int getAssentos() {
        return Assentos;
    }

    public void setAssentos(int assentos) {
        Assentos = assentos;
    }
}
